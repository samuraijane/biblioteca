module.exports = (app, passport) => {

  // -----------------------------------------------------------------------------
  //                                LOGIN
  // -----------------------------------------------------------------------------
  app.get('/auth/github', passport.authenticate('github'));

  // -----------------------------------------------------------------------------
  //                                CALLBACK
  // -----------------------------------------------------------------------------
  app.get(
    '/auth/github/callback',
    passport.authenticate('github', {failureRedirect: '/'}),
    (req, res) => res.redirect('/dashboard')
  );

  // -----------------------------------------------------------------------------
  //                                LOGOUT
  // -----------------------------------------------------------------------------
  app.get('/auth/logout', (req, res) => {
    req.logout()
    res.redirect("/")
  });

};